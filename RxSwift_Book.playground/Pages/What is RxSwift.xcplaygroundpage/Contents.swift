 # RxSwift 란?
 
 이전에는 ReactiveX에 대해서 알아보았습니다.
 
 그렇다면 RxSwift는 무엇일까요? 감이 좀 오지 않으시나요?
 
 바로 ReactiveX + Swift 입니다.
 
 ReactiveX는 이전에 설명했던것 처럼 __Observable Stream__ 을 사용하여 비동기 처리를 하는 라이브러리입니다.
 
 그리고 Swift는 애플이 만든 프로그래밍 언어이며, 객체지향 프로그래밍 및 함수형 프로그래밍을 모두 지원하는
 
 멀티 패러다임 언어입니다. 여기서의 '패러다임' 은 틀이나 체계 정도로 이해하시면 될 것 같습니다.
 
 이 두가지가 합쳐진게 RxSwift인데 RxSwift 페이지에는 이런 말이 있습니다.

 "RxSwift is the Swift-specific implementation of the Reactive Extensions standard."
 
 ReactiveX의 표준을 따르는 Swift라고 하니까 결론적으로 RxSwift는 Swift언어로 비동기 프로그래밍을 할 수 있게 해주는 ReactiveX 라이브러리 라고 할 수 있겠네요.
 
 ReactiveX의 오퍼레이터나 동작 방식들을 보면 스위프트의 고차함수들을 이용하니까 좀 더 명확하게는 스위프트의 함수형 프로그래밍을 확장하여 비동기 프로그래밍을 할 수 있게 해 주는 라이브러리! 라고 생각되네요!
 
